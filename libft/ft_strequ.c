#include "libft.h"

int ft_strequ(char const *s1, char const *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		if (s1[i] == s2[j])
			j++;
		i++;
	}
	if (i == j)
		return (1);
	return (0);
}
