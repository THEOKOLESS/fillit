#include <string.h>

int ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (n == 0)
		return (0);
	while (s1[i] && s1[i] == s2[j] && n != 1)
	{
		i++;
		j++;
		n--;
	}
	return (s1[i] - s2[j]);
}
