#include "libft.h"
void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*((char *)s1 + i) = *((const char *)s2 + i);
		i++;
	}
	return (s1);
}

