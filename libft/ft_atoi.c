#include "libft.h"
int	ft_atoi(const char *s)
{
	int	i;
	int	n;
	int	r;

	i = 0;
	n = 1;
	r = 0;
	while (s[i])
	{
		if ((s[i] < '0' || s[i] > '9') && s[i] != '-' && s[i] != ' '
				&& s[i] != '+')
			return (0);
		if ((s[i] == '-' || s[i] == '+') && (s[i + 1] < '0' || s[i + 1] > '9'))
			return (0);
		if (s[i] == '-' && (s[i + 1] >= '0' && s[i + 1] <= '9') && r == 0)
			n = -1;
		if (s[i] >= '0' && s[i] <= '9' && (r = r * 10 + s[i] - '0'))
			if (s[i + 1] < '0' || s[i + 1] > '9')
				return (r * n);
		i++;
	}
	return (r * n);
}
